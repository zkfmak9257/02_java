package com.qew032.section02.assignment;

public class Application1 {

  public static void main(String[] args) {

    /***********************************************************************************************************************************
    *  대입 연산자(=)
     *  - 복합 대임 연산자 ( A += | -= | *= | /= | %= B)
    ***********************************************************************************************************************************/

    int num =12;
    System.out.println("num = " + num);

    num += 3;
    System.out.println("num = " + num);

    num -= 5;
    System.out.println("num = " + num);

    num *= 2;
    System.out.println("num = " + num);

    num /=  6;
    System.out.println("num = " + num);

    num %= 2;
    System.out.println("num = " + num);

  }

}
